  const bgColor = '#0a0d18';
  let cols = clamp(Number(canvasColsInput.value) || 32, 8, 64);
  let rows = clamp(Number(canvasRowsInput.value) || 32, 8, 64);
  let pixels = new Array(cols * rows).fill(bgColor);
  let currentTool = 'brush';
  let currentColor = '#ff3d7f';
  let gradientStart = '#ff3d7f';
  let gradientEnd = '#59d6ff';
  let isDrawing = false;
  let animationTime = 0;
  let frameRequest = null;

  pixelCanvas.width = 640;
