const langButtons = document.querySelectorAll('.lang-button');
const languageOrder = ['en', 'ru', 'zh'];
const languageFlags = { en: '🇬🇧', ru: '🇷🇺', zh: '🇨🇳' };

function updateLangButtons(lang) {
  langButtons.forEach((btn) => {
    btn.dataset.lang = lang;
    btn.textContent = languageFlags[lang] || lang.toUpperCase();
  });
}

function applyLanguage(lang) {
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach((element) => {
    const key = element.dataset.i18n;
    if (!key) return;
    const text = translations[lang]?.[key] || translations.en[key] || element.textContent;
    element.textContent = text;
  });
  document.documentElement.lang = lang;
  localStorage.setItem('mcsLang', lang);
  if (langButtons.length === 1) {
    updateLangButtons(lang);
  } else {
    langButtons.forEach((btn) => btn.classList.toggle('active', btn.dataset.lang === lang));
  }
}

if (langButtons.length) {
  const savedLang = localStorage.getItem('mcsLang') || 'en';
  applyLanguage(savedLang);
  if (langButtons.length === 1) {
    langButtons[0].addEventListener('click', () => {
      const current = langButtons[0].dataset.lang || 'en';
      const nextIndex = (languageOrder.indexOf(current) + 1) % languageOrder.length;
      applyLanguage(languageOrder[nextIndex]);
    });
  } else {
    langButtons.forEach((button) => {
      button.addEventListener('click', () => {
        applyLanguage(button.dataset.lang);
      });
    });
  }
}

